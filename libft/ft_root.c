#include "libft.h"

int		ft_root(int a)
{
	long long	b;

	if (a == 0)
		return (0);
	if (a < 0)
		return (-1);
	b = 1;
	while (b * b < a)
		b++;
	if (b * b > a)
		b--;
	return (b);
}
