#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*list;
	t_list	*tmp;

	if (!lst || !f)
		return (NULL);
	list = f(lst);
	tmp = list;
	while (lst->next != NULL)
	{
		lst = lst->next;
		if (!(list->next = f(lst)))
		{
			free(list->next);
			return (NULL);
		}
		list = list->next;
	}
	list->next = NULL;
	return (tmp);
}
